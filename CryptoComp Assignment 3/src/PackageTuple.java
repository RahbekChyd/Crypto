
public class PackageTuple {
	    private int u;
	    private int v;
	    private int w;

	    public PackageTuple(int u, int v, int w) {
	        this.u = u;
	        this.v = v;
	        this.w = w;
	    }

	    public int getV() {
	        return v;
	    }

	    public int getW() {
	        return w;
	    }
	    
	    public int getU() {
	        return u;
	    }
	    
	    public void setV(int x) {
	        v = x;
	    }

	    public void setW(int x) {
	        w = x;
	    }
	    
	    public void setU(int x) {
	        u = x;
	    }
}
